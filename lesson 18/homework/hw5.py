#ივარჯიშეთ if/elif/else-ებზე და გააკეთეთ რამოდენიმე მაგალითი მათზეც

num=int(input("შეიყვანე შენი რიცხვი "))

if num % 2==0:
    print("შენი რიცხვი ლუწია")
else:
    print("შენი რიცხვი კენტია")





# == 
print(5 == 5)   # True 
print(5 == 3)   # False

# !=  
print(5 != 3)   #True
print(4 != 4)   #False

# > 
print(7 > 2)    # True
print(3 > 9)    # False

# < 
print(2 < 5)    # True
print(8 < 1)    # False

# >=  
print(5 >= 5)   # True 
print(6 >= 2)   # True 
print(1 >= 4)   # False

# <= 
print(3 <= 3)   # True 
print(2 <= 10)  # True  
print(9 <= 4)   # False
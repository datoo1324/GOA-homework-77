#კომენტარებით ახსენით რა არის flowchart-ი, pseudo code და natural language.
# შეეცადეთ შექმნათ მარტივი ალგორითმი(კოდი რომელიც რაღაც დავალებას შეასრულებს), შემდეგ კი აღწეროთ ის სამივე ტექნიკის გამოყენებით.
#flowchart არის ალგორითმის გრაფიკული გამოსახვა.
#pseudo code არის ალგორითმის აღწერა ჩვეულებრივი სიტყვებით და პროგრამირების სტილის შერევით.
#natural language არის ალგორითმის აღწერა მხოლოდ ბუნებრივი, ჩვეულებრივი ენით ქართულად ინგლისურად და ასეშემდეგ.
